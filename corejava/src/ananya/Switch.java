package ananya;

public class Switch {
public static void main(String[] args) {
	
	switch(3)
	{
	case 1: 
	{
		System.out.println("case 1 is executed");
		break;
	}
	case 2: 
	{
		System.out.println("case 2 is executed");
		break;
	}
	case 3: 
	{
		System.out.println("case 3 is executed");
		break;
	}
	default:
	{
		System.out.println("no case is executed");
		break;
	}
}
}
}